/*
========================================
TOPIC NAME:
For Loops
========================================

DEFINITION (Simple English):
----------------------------------------
A loop is used to repeat a block of code
multiple times.

CONCEPT + REAL LIFE EXAMPLE (Roman Urdu):
----------------------------------------
Jab aik kaam baar baar karna ho,
loop use karte hain.

Real life example:
Attendance lagana.

SYNTAX:
----------------------------------------
for(initialization; condition; increment)
*/

// ================================
// PROGRAM CODE
// ================================

void main() {
  for (int i = 1; i <= 5; i++) {
    print(i);
  }
}

/*
OUTPUT:
----------------------------------------
1
2
3
4
5
========================================
